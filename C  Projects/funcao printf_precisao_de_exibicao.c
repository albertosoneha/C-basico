
int main(){

    int x = 450;
    float f = 250.12345;
    char s[] = "Boson treinamentos";
    printf("Precisao em inteiros: \n");
    printf("%d\n", x);
    printf("%.5d\n", x);
    printf("%.12d\n", x);
    printf("\n\n Precisao em ponto-flutuante: \n");
    printf("%f\n", f);
    printf("%.2f\n", f);
    printf("\n Precisao em literais: \n");
    printf("%s\n", s);
    printf("%.5s\n", s);
    printf("%.18s\n", s);
    return 0;







}